/* USER CODE BEGIN Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */

  rmw_uros_set_custom_transport(
    true,
    (void *) &huart6,
    cubemx_transport_open,
    cubemx_transport_close,
    cubemx_transport_write,
    cubemx_transport_read);

  rcl_allocator_t freeRTOS_allocator = rcutils_get_zero_initialized_allocator();
  freeRTOS_allocator.allocate = microros_allocate;
  freeRTOS_allocator.deallocate = microros_deallocate;
  freeRTOS_allocator.reallocate = microros_reallocate;
  freeRTOS_allocator.zero_allocate =  microros_zero_allocate;

  if (!rcutils_set_default_allocator(&freeRTOS_allocator)) {
  }

  rclc_support_t support;
  rcl_allocator_t allocator;
  rcl_node_t node;
  rclc_executor_t executor;

  allocator = rcl_get_default_allocator();

  RCCHECK(rclc_support_init(&support, 0, NULL, &allocator));
  RCCHECK(rclc_node_init_default(&node, "stm32_encoder_node", "", &support));

  RCCHECK(rclc_publisher_init_best_effort(
      &publisher_speed,
      &node,
      ROSIDL_GET_MSG_TYPE_SUPPORT(std_msgs, msg, Float32),
      "wheel_speed"));

  executor = rclc_executor_get_zero_initialized_executor();
  RCCHECK(rclc_executor_init(&executor, &support.context, 0, &allocator));

  HAL_TIM_Base_Start(&htim2);
  for(;;)
  {
    // LED TEST: Döngü yaşıyor mu?
    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);

    // Micro-ROS ile konuş
    rclc_executor_spin_some(&executor, RCL_MS_TO_NS(1000000));

    // HIZ ÖLÇÜMÜ (100 ms periyotla)
    if (HAL_GetTick() - last_measure_time >= 100)
    {
        // 1. Sayacı Oku (TIM2 - 32 Bit)
        // Artık cast işlemine gerek yok, çünkü TIM2 32 bit.
        suanki_sayac = __HAL_TIM_GET_COUNTER(&htim2);

        // 2. Farkı Hesapla (32-Bit Unsigned Matematik)
        // Eğer sayaç 4 Milyar'dan 0'a dönerse bile bu işlem (unsigned olduğu için)
        // otomatik olarak doğru farkı verir.
        fark = suanki_sayac - onceki_sayac;
        onceki_sayac = suanki_sayac;

        // 3. RPM Hesabı
        float saniyedeki_tur = (float)fark / DISK_DELIK_SAYISI;
        float ham_rpm = saniyedeki_tur * 10.0 * 60.0;

        // 4. Filtreleme
        hiz_gecmisi[filtre_indeksi] = ham_rpm;
        filtre_indeksi = (filtre_indeksi + 1) % FILTRE_BOYUTU;
        float toplam = 0;
        for (int i = 0; i < FILTRE_BOYUTU; i++) {
            toplam += hiz_gecmisi[i];
        }

        // 5. Veriyi Gönder
        msg_speed.data = toplam / FILTRE_BOYUTU;

        // Yayınla
        rcl_publish(&publisher_speed, &msg_speed, NULL);

        last_measure_time = HAL_GetTick();
    }

    osDelay(10);
  }
  /* USER CODE END 5 */
}

